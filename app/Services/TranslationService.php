    /**
     * Translate an English word/phrase to the user's UI language.
     */
    public function translate(string $english, string $uiLanguage): string
    {
        if ($uiLanguage === 'en') return $english;

        $dict = $this->getDictionary($uiLanguage);
        $key = strtolower(trim($english));

        return $dict[$key] ?? $english;
    }

    /**
     * Translate an array of word pairs: replace 'translation' with UI language.
     */
    public function translatePairs(array $wordPairs, string $uiLanguage): array
    {
        if ($uiLanguage === 'en') return $wordPairs;

        return array_map(function ($pair) use ($uiLanguage) {
            $pair['translation'] = $this->translate($pair['translation'], $uiLanguage);
            return $pair;
        }, $wordPairs);
    }

    /**
     * Get available UI languages.
     */
    public static function availableLanguages(): array
    {
        return [
            'en' => 'English',
            'nl' => 'Nederlands',
            'tr' => 'Türkçe',
        ];
    }

    private function getDictionary(string $lang): array
    {
        return match ($lang) {
            'nl' => $this->dutch(),
            'tr' => $this->turkish(),
            default => [],
        };
    }

